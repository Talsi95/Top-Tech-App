const generateLegalDocument = (template, store) => {
    let result = template;

    const storeName = store.name || 'החנות שלנו';
    const storeEmail = store.businessInfo?.email || '';
    const storePhone = store.businessInfo?.phone || '';
    const storeAddress = store.businessInfo?.address || '';
    const companyNumber = store.businessInfo?.companyNumber || '';

    const companyText = companyNumber ? `ח.פ/ע.מ ${companyNumber}` : '';
    const phoneText = storePhone ? `<li><strong>טלפון:</strong> ${storePhone}</li>` : '';
    const addressText = storeAddress ? `<li><strong>כתובת העסק:</strong> ${storeAddress}</li>` : '';

    result = result
        .replace(/{{STORE_NAME}}/g, storeName)
        .replace(/{{STORE_EMAIL}}/g, storeEmail)
        .replace(/{{COMPANY_NUMBER_TEXT}}/g, companyText)
        .replace(/{{STORE_PHONE_TEXT}}/g, phoneText)
        .replace(/{{STORE_ADDRESS_TEXT}}/g, addressText);

    return result;
};

